import pickle
import streamlit as st
import pandas as pd
import numpy as np

model = pickle.load(open('salary_pred.pkl','rb'))

def predict_salary(sex,designation,age,unit,leaves_u,leaves_r,ratings,past_exp):
  input=np.array([[sex,designation,age,unit,leaves_u,leaves_r,ratings,past_exp]])
  prediction=model.predict(input)
  return prediction

def main():
  st.title("Salary Predictor")
  html_temp = """
  <div>
  <h2 style="color:white;text-align:centre;">Salary Predictor</h2>
  </div>
  """
  st.markdown(html_temp , unsafe_allow_html=True)

  sex=st.text_input("SEX ----------> [0 For Female and 1 For Male ]" ,placeholder="Type Here")
  designation=st.text_input("DESIGNATION" ,placeholder="Type Here")
  age=st.text_input("AGE" ,placeholder="Type Here")
  unit=st.text_input("UNIT" ,placeholder="Type Here")
  leaves_u=st.text_input("LEAVES USED" ,placeholder="Type Here")
  leaves_r=st.text_input("LEAVES REMAINING" ,placeholder="Type Here")
  ratings=st.text_input("RATINGS" ,placeholder="Type Here")
  past_exp=st.text_input("PAST EXPERIANCE" ,placeholder="Type Here")

  unsafe_qual ="""
  <div style="background-color:red; padding:10px;> 
  <h2 style="color:white;text-align:centre;">Wine Quality is Bad</h2>
  </div>
  """
  safe_qual="""
  <div style="background-color:green; padding:10px;> 
  <h2 style="color:white;text-align:centre;">Wine Quality is Good</h2>
  </div>
  """

  if st.button("Check"):
    output=predict_salary(sex,designation,age,unit,leaves_u,leaves_r,ratings,past_exp).astype('int64')
    st.success('The Predicted Salary is : {} Rupees'.format(output))

if __name__ =='__main__':
  main()
  